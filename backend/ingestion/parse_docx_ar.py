def build_chunks():
    chunks = []
    for section in SECTIONS:
        title   = section["title"]
        content = CONTENT.get(title, "").strip()
        if not content:
            print(f"  [WARN] No content for: {title}")
            continue

        chunks.append({
            "title":             title,
            "content":           content,
            "source_type":       "canonical_doc",
            "source_name":       section["source_name"],
            "topic":             section["topic"],
            "intent_categories": section["intent_categories"],
            "priority":          section["priority"],
            "approved":          True,
        })
        print(f"  [OK] {title} — {len(content)} chars")

    return chunks


if __name__ == "__main__":
    os.makedirs("data/processed", exist_ok=True)
    print(f"\n[PARSING] Divido_Info_.docx → Arabic knowledge chunks\n")
    chunks = build_chunks()
    with open(OUTPUT, "w", encoding="utf-8") as f:
        json.dump(chunks, f, ensure_ascii=False, indent=2)
    print(f"\n✅ Done. {len(chunks)} chunks saved to {OUTPUT}")
